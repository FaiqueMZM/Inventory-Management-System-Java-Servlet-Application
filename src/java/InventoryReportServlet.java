    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    private static DatabaseQueries dbQueries = new DatabaseQueries();

    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            // Retrieve the inventory items from the database
            List<Item> inventoryItems = dbQueries.retInventoryR();

            // Set the retrieved inventory items as an attribute in the request
            request.setAttribute("inventoryItems", inventoryItems);

            // Forward the request to the inventory report JSP page
//            request.getRequestDispatcher("inventoryReport.jsp").forward(request, response);
            RequestDispatcher requestDispatcher = request.getRequestDispatcher("inventoryReport.jsp");
            requestDispatcher.forward(request, response);
        } catch (SQLException e) {
            e.printStackTrace();
            // Handle the exception and display an error page
            response.getWriter().println("Error retrieving inventory items: " + e.getMessage());
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

